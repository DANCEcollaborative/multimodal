        /// The ring distal joint.
        /// </summary>
        RingDistal,

        /// <summary>
        /// The ring tip joint.
        /// </summary>
        RingTip,

        /// <summary>
        /// The pink metacarpal joint.
        /// </summary>
        PinkyMetacarpal,

        /// <summary>
        /// The pinky proximal joint.
        /// </summary>
        PinkyProximal,

        /// <summary>
        /// The pinky intermediate joint.
        /// </summary>
        PinkyIntermediate,

        /// <summary>
        /// The pinky distal joint.
        /// </summary>
        PinkyDistal,

        /// <summary>
        /// The pinky tip joint.
        /// </summary>
        PinkyTip,

        /// <summary>
        /// Maximum index value.
        /// </summary>
        MaxIndex,
    }
}
