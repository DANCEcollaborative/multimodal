        /// </summary>
        Palm = 0,

        /// <summary>
        /// The wrist joint.
        /// </summary>
        Wrist,

        /// <summary>
        /// The thumb metacarpal joint.
        /// </summary>
        ThumbMetacarpal,

        /// <summary>
        /// The thumb proximal joint.
        /// </summary>
        ThumbProximal,

        /// <summary>
        /// The thumb distal joint.
        /// </summary>
        ThumbDistal,

        /// <summary>
        /// The thumb tip joint.
        /// </summary>
        ThumbTip,

        /// <summary>
        /// The index metacarpal joint.
        /// </summary>
        IndexMetacarpal,

        /// <summary>
        /// The index proximal joint.
        /// </summary>
        IndexProximal,

        /// <summary>
        /// The index intermediate joint.
        /// </summary>
        IndexIntermediate,

        /// <summary>
        /// The index distal joint.
        /// </summary>
        IndexDistal,

        /// <summary>
        /// The index tip joint.
        /// </summary>
        IndexTip,

        /// <summary>
        /// The middle metacarpal joint.
        /// </summary>
        MiddleMetacarpal,

        /// <summary>
        /// The middle proximal joint.
        /// </summary>
        MiddleProximal,

        /// <summary>
        /// The middle intermediate joint.
        /// </summary>
        MiddleIntermediate,

